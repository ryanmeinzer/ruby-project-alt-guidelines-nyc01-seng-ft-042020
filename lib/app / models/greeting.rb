def greeting(name)
    puts "What up, #{name.chomp.strip}. Let's hit Trail 1 together. Saddle up and follow me. Remember to hit that stopwatch to track your time on this run."
    puts "|          |"
    puts " |        |"
    puts "  |      |"
    puts "   |    |"
    puts "    |  |"
    puts "Siiiick run. How fast did you do that run in? Just enter the amount of seconds."
end

def second_message(time)
